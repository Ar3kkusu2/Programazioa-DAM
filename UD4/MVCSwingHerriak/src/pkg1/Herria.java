package pkg1;

/**
 *
 * @author moreno.manuel
 */
public class Herria {
    
    private String Herria;
    private String Probintzia;
    private boolean Hondartza;
    private String Oharra;

    public Herria(String Herria, String Probintzia, boolean Hondartza, String Oharra) {
        this.Herria = Herria;
        this.Probintzia = Probintzia;
        this.Hondartza = Hondartza;
        this.Oharra = Oharra;
    }

    public String getHerria() {
        return Herria;
    }

    public void setHerria(String Herria) {
        this.Herria = Herria;
    }

    public String getProbintzia() {
        return Probintzia;
    }

    public void setProbintzia(String Probintzia) {
        this.Probintzia = Probintzia;
    }

    public boolean isHondartza() {
        return Hondartza;
    }

    public void setHondartza(boolean Hondartza) {
        this.Hondartza = Hondartza;
    }

    public String getOharra() {
        return Oharra;
    }

    public void setOharra(String Oharra) {
        this.Oharra = Oharra;
    }
    
    
}
